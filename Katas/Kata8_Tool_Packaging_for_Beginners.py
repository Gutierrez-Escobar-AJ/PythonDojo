# utils.py
def count_lines(file_path):
    """Count the number of non-empty lines in a file."""
    count = 0
    try:
        with open(file_path, "r") as f:
            for line in f:
                if line.strip():
                    count += 1
    except FileNotFoundError:
        print(f"âŒ File not found: {file_path}")
    return count

# main.py
def main():
    from utils import count_lines

    input_path = "data/sample_data.txt"
    output_path = "results/output.txt"

    print("ğŸ“ Loading input file...")

    line_count = count_lines(input_path)
    print(f"âœ… Total non-empty lines: {line_count}")

    # Save result to file
    with open(output_path, "w") as f:
        f.write(f"Non-empty line count: {line_count}\n")

    print(f"ğŸ“„ Saved result to: {output_path}")

